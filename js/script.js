$("#lobster").hide();
$("#sun").hide();  

$("#red").click(function(){
   $("#box").css("background-color", "red");
   $("#lobster").show();
   $("#sun").hide();  
  
});

$("#yellow").click(function(){
   $("#box").css("background-color", "yellow");
    $("#sun").show();  
    $("#lobster").hide();
    
    
});

